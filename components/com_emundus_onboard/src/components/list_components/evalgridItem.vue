<template class="form-item">
  <div class="main-column-block">
    <div class="column-block w-100">
      <div class="block-dash" >
        <div class="column-blocks w-row">
          <div class="column-inner-block w-col w-col-8 pl-30px">
            <div class="list-item-header">
              <div class="block-label">
                <a class="item-select w-inline-block"
                   v-on:click="selectItem(data.id)"
                   :class="{ active: isActive }">
                </a>
                <h2 class="nom-campagne-block">{{ data.label[actualLanguage] }}</h2>
              </div>
              <!--<div :class="isPublished ? 'publishedTag' : 'unpublishedTag'">
                {{ isPublished ? translations.publishedTag : translations.unpublishedTag }}
              </div>-->
            </div>
            <div>

              <ul style="margin-top: 10px">
                <li  class="campaigns-item">{{campaigns.label}}</li>
              </ul>
            </div>
            <div class="stats-block" style="justify-content: flex-end">
              <a class="bouton-ajouter pointer add-button-div"
                 @click="evaluationBuilder"
                 :title="translations.Modify">
                <em class="fas fa-pen"></em> Ã‰diter
              </a>
            </div>
          </div>
        </div>
      </div>
    </div>
  </div>
</template>

<script>
import { list } from "../../store";
import axios from "axios";

const qs = require("qs");

export default {
  name: "grilleEvalItem",
  props: {
    data: Object,
    selectItem: Function,
    actualLanguage:String
  },
  data() {
    return {
      selectedData: [],
      updateAccess: false,
      campaigns: [],
      translations:{
        publishedTag: Joomla.JText._("COM_EMUNDUS_ONBOARD_FILTER_PUBLISH"),
        unpublishedTag: Joomla.JText._("COM_EMUNDUS_ONBOARD_FILTER_UNPUBLISH"),
        Modify: Joomla.JText._("COM_EMUNDUS_ONBOARD_MODIFY"),
        campaignAssociated: Joomla.JText._("COM_EMUNDUS_ONBOARD_CAMPAIGN_ASSOCIATED"),
        campaignsAssociated: Joomla.JText._("COM_EMUNDUS_ONBOARD_CAMPAIGNS_ASSOCIATED")
      },
    };
  },

  methods: {
    redirectJRoute(link) {
      axios({
        method: "get",
        url: "index.php?option=com_emundus_onboard&controller=settings&task=redirectjroute",
        params: {
          link: link,
        },
        paramsSerializer: params => {
          return qs.stringify(params);
        }
      }).then(response => {
        window.location.href = window.location.pathname + response.data.data;
      });
    },

    getAssociatedCampaigns(){
      axios({
        method: "get",
        url: "index.php?option=com_emundus_onboard&controller=form&task=getassociatedprogram",
        params: {
          fid: this.data.id,
        },
        paramsSerializer: params => {
          return qs.stringify(params);
        }
      }).then(response => {

        this.campaigns = response.data.data;
        //cons
      });
    },
    evaluationBuilder() {

      this.redirectJRoute('index.php?option=com_emundus_onboard&view=form&layout=formbuilder&prid=&index=0&cid=' +
          '' +
          '&evaluation=' +
          this.data.id);
    },

  },

  created() {




    list.getters.formsAccess[0].forEach(element => {
      if(element === this.data.id){
        this.updateAccess = true;
      }
    });

    this.getAssociatedCampaigns();
  },

  computed: {
    isPublished() {
      return this.data.status == 1;
    },

    isActive() {
      return list.getters.isSelected(this.data.id);
    }
  }
};
</script>
