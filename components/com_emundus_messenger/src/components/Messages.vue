<template>
  <div class="messages__vue">
    <span :id="'messages'">
      <modal
          :name="'messages'"
          transition="nice-modal-fade"
          :adaptive="true"
          height="auto"
          width="45%"
          :scrollable="true"
          :delay="100"
          :clickToClose="true"
          :draggable="'.drag-window'"
          @closed="beforeClose"
          @opened="getFilesByUser"
      >
        <div class="drag-window">
          <div class="col-md-5 messages__campaigns-list">
            <div v-for="file in files" @click="fileSelected = file.fnum" :class="file.fnum == fileSelected ? 'messages__active-campaign' : ''" class="messages__block">
              <div class="messages__campaign-block">
                <img class="messages__campaigns_folder-icon" src="/images/emundus/messenger/folder.svg" />
                <div style="margin-left: 10px;">
                  <p class="messages__campaigns_title">{{file.label}}</p>
                  <p class="messages__campaigns_fnum messages__campaigns_title">NÂ° {{file.fnum}}</p>
                  <p class="messages__campaigns_fnum messages__campaigns_title">{{file.year}}</p>
                </div>
              </div>
              <div></div>
              <AttachDocument :user="user" :fnum="file.fnum" :applicant="true" @pushAttachmentMessage="pushAttachmentMessage"/>
            </div>
          </div>

          <div class="messages__list col-md-7">
            <div class="message__header">
              <label class="text-center" style="width: 100%">{{translations.messages}}</label>
              <i class="fas fa-times pointer" @click="$modal.hide('messages')"></i>
            </div>
            <div class="messages__list-block" id="messages__list">
<!--              <infinite-loading direction="top" @infinite="infiniteHandler"></infinite-loading>-->
              <div v-for="date in dates">
                <div class="messages__date-section">
                  <hr>
                  <p>{{ moment(date.dates).format("DD/MM/YYYY") }}</p>
                  <hr>
                </div>
                <div v-for="message in messages" v-if="date.messages.includes(message.message_id)" class="messages__message-item" :class="user == message.user_id_from ? 'messages__current_user' : 'messages__other_user'">
                  <div class="messages__message-item-block" @click="showDate != message.message_id ? showDate = message.message_id : showDate = 0" :class="user == message.user_id_from ? 'messages__text-align-right' : 'messages__text-align-left'">
                    <p><span class="messages__message-item-from">{{message.name}} - {{ moment(message.date_time).format("HH:mm") }}</span></p>
                    <span class="messages__message-item-span" :class="user == message.user_id_from ? 'messages__message-item-span_current-user' : 'messages__message-item-span_other-user'" v-html="message.message"></span>
                    <p><span class="messages__message-item-from" v-if="showDate == message.message_id">{{ moment(message.date_time).format("DD/MM/YYYY HH:mm") }}</span></p>
                  </div>
                </div>
              </div>
            </div>
            <div class="messages__bottom-input">
              <textarea type="text" class="messages__input_text" rows="1" spellcheck="true" :placeholder="translations.writeMessage" v-model="message" @keyup.enter.exact.prevent="sendMessage($event)"/>
            </div>
            <div class="messages__bottom-input-actions">
              <div class="messages__actions_bar">
                <img class="messages__send-icon" src="/images/emundus/messenger/attached.svg" @click="attachDocument"/>
              </div>
              <button type="button" class="messages__send_button" @click="sendMessage">
                  {{ translations.send }}
              </button>
            </div>
          </div>
        </div>
      </modal>
    </span>
    <div class="loader" v-if="loading"></div>
  </div>
</template>

<script>
import axios from "axios";
import InfiniteLoading from 'vue-infinite-loading';
import moment from 'moment';

import "../assets/css/bootstrap.css";
import "../assets/css/messenger.scss";

import AttachDocument from "../modals/AttachDocument";

const qs = require("qs");

export default {
  name: "Messages",
  props: {
    user: Number,
    fnum: String,
    notifications: Object,
  },
  components: {
    InfiniteLoading,
    AttachDocument
  },
  data() {
    return {
      dates: [],
      messages: [],
      files: [],
      fileSelected: 0,
      message: '',
      loading: false,
      interval: 0,
      showDate: 0,
      translations:{
        messages: Joomla.JText._("COM_EMUNDUS_MESSENGER_TITLE"),
        send: Joomla.JText._("COM_EMUNDUS_MESSENGER_SEND"),
        writeMessage: Joomla.JText._("COM_EMUNDUS_MESSENGER_WRITE_MESSAGE"),
      }
    };
  },

  methods: {
    moment(date) {
      return moment(date);
    },

    beforeClose() {
      clearInterval(this.interval);
    },

    getFilesByUser(){
      axios({
        method: "get",
        url: "index.php?option=com_emundus_messenger&controller=messages&task=getfilesbyuser",
      }).then(response => {
        this.files = response.data.data;
        if(this.fnum != ''){
          this.fileSelected = this.fnum;
        } else {
          this.fileSelected = this.files[0].fnum;
        }
        this.getMessagesByFnum(true);
        this.interval = setInterval(() => {
          this.getMessagesByFnum(false,false);
        },20000);
      });
    },

    getMessagesByFnum(loader = true,scroll = true){
      this.loading = loader;
      axios({
        method: "get",
        url: "index.php?option=com_emundus_messenger&controller=messages&task=getmessagesbyfnum",
        params: {
          fnum: this.fileSelected,
        },
        paramsSerializer: params => {
          return qs.stringify(params);
        }
      }).then(response => {
        this.messages = response.data.data.messages;
        this.dates = response.data.data.dates;
        this.markAsRead();
        if(scroll) {
          this.scrollToBottom();
        }
        this.loading = false;
      });
    },

    markAsRead(){
      axios({
        method: "get",
        url: "index.php?option=com_emundus_messenger&controller=messages&task=markasread",
        params: {
          fnum: this.fileSelected,
        },
        paramsSerializer: params => {
          return qs.stringify(params);
        }
      }).then(response => {
        this.$emit('removeNotifications',response.data.data);
      });
    },

    sendMessage(e){
      if(typeof e != 'undefined') {
        e.stopImmediatePropagation();
      }
      if(this.message !== '') {
        axios({
          method: "post",
          url:
              "index.php?option=com_emundus_messenger&controller=messages&task=sendmessage",
          headers: {
            "Content-Type": "application/x-www-form-urlencoded"
          },
          data: qs.stringify({
            message: this.message,
            fnum: this.fileSelected
          })
        }).then(response => {
          this.message = '';
          this.pushToDatesArray(response.data);
          this.scrollToBottom();
        });
      }
    },

    pushToDatesArray(message){
      var pushToDate = false;
      var message_date = message.date_time.split(' ')[0];
      this.dates.forEach((elt,index) => {
        if(elt.dates == message_date){
          this.dates[index].messages.push(message.message_id);
          pushToDate = true;
        }
      });
      if(!pushToDate){
        var new_date = {
          dates: this.moment().format("YYYY-MM-DD"),
          messages: []
        }
        new_date.messages.push(message.message_id);
        this.dates.push(new_date);
      }
      this.messages.push(message);
    },

    /*infiniteHandler($state){
      setTimeout(() => {
            axios({
              method: "get",
              url: "index.php?option=com_emundus_messenger&controller=messages&task=getmessagesbyfnum",
              params: {
                fnum: this.campaignSelected,
                offset: this.messages.length,
              },
              paramsSerializer: params => {
                return qs.stringify(params);
              }
            }).then(response => {
              this.messages.unshift(response.data.data);
              this.markAsRead();
              $state.loaded();
            });
      },1000);
    },*/

    scrollToBottom() {
      setTimeout(() => {
        const container = document.getElementById("messages__list");
        container.scrollTop = container.scrollHeight;
      },500);
    },

    attachDocument(){
      this.$modal.show('attach_documents' + this.fileSelected);
    },

    pushAttachmentMessage(message){
      this.$modal.hide('attach_documents' + this.fileSelected);
      this.pushToDatesArray(message);
      this.scrollToBottom();
    }
  },

  watch: {
    fileSelected: function(){
      this.getMessagesByFnum(true);
    }
  }
}
</script>
